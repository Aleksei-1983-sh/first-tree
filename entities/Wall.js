// Сейчас активная версия конструкции живёт в core/Game.js, чтобы Этап 3 оставался простым.
export class Wall {
    /**
     * Сохраняет зависимости и настройки будущей стены без создания реальных блоков.
     */
    constructor(scene, physics, options) {
        this.scene = scene;
        this.physics = physics;
        this.options = options;
        this.cubes = [];
    }

    /**
     * Позже будет собирать стену из кубов и возвращать созданные элементы.
     */
    build() {}

    /**
     * Позже будет очищать кубы стены из сцены и физического мира.
     */
    destroy() { this.cubes = []; }
}
